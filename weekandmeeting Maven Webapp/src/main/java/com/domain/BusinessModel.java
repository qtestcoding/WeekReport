/**
 * @author cuiyueyang
 * @data 2016-3-7 下午4:51:02
 * @todo TODO
 */
public class BusinessModel {

	private String businessname;
	private String productname;
	private String priority;
	private String progress;
	private String bugnum;
	private String person;
	private String businessdescription;
	/**
	 * @return the businessname
	 */
	public String getBusinessname() {
		return businessname;
	}
	/**
	 * @param businessname the businessname to set
	 */
	public void setBusinessname(String businessname) {
		this.businessname = businessname;
	}
	/**
	 * @return the productname
	 */
	public String getProductname() {
		return productname;
	}
	/**
	 * @param productname the productname to set
	 */
	public void setProductname(String productname) {
		this.productname = productname;
	}
	/**
	 * @return the priority
	 */
	public String getPriority() {
		return priority;
	}
	/**
	 * @param priority the priority to set
	 */
	public void setPriority(String priority) {
		this.priority = priority;
	}
	/**
	 * @return the progress
	 */
	public String getProgress() {
		return progress;
	}
	/**
	 * @param progress the progress to set
	 */
	public void setProgress(String progress) {
		this.progress = progress;
	}
	/**
	 * @return the bugnum
	 */
	public String getBugnum() {
		return bugnum;
	}
	/**
	 * @param bugnum the bugnum to set
	 */
	public void setBugnum(String bugnum) {
		this.bugnum = bugnum;
	}
	/**
	 * @return the person
	 */
	public String getPerson() {
		return person;
	}
	/**
	 * @param person the person to set
	 */
	public void setPerson(String person) {
		this.person = person;
	}
	/**
	 * @return the businessdescription
	 */
	public String getBusinessdescription() {
		return businessdescription;
	}
	/**
	 * @param businessdescription the businessdescription to set
	 */
	public void setBusinessdescription(String businessdescription) {
		this.businessdescription = businessdescription;
	}
}
